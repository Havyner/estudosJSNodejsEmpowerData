// Adição +

let numero1 = 10
let numero2 = 2

let resultadoad = numero1 + numero2

console.log(resultadoad)

// Subtração - 

let resultadosub = numero1 - numero2

console.log(resultadosub)

// Multiplicação *

let resultadomul = numero1 * numero2

console.log(resultadomul)

// Divisão / 

let resultadodiv = numero1 / numero2

console.log(resultadodiv)

// Resto da divisão %

let resultadorest = numero1 % numero2

console.log(resultadorest)

// Exponenciação **

let resultadoexpo = numero1 ** numero2

console.log(resultadoexpo)

